    # 诸葛殿高大而宏敞。飞檐凌空，气势不凡。门外有细砂石栏，有纹铁香炉，有历代名流题下的楹联。门楣上，迎面一幅巨匾，灿然书写着四个闪金的大字――“名垂宇宙”。
