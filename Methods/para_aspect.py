def seg_doc(document):
    p = pipeline(
        task=Tasks.document_segmentation,
        model='damo/nlp_bert_document-segmentation_chinese-base')

    result = p(documents=document)
    print(result)
    print("=-===============")
    return result[OutputKeys.TEXT]


def seg_to_chunks(document, max_chars=350):
    chunks = []
    paragraphs = document.split('\n')
    current_chunk = ""
    # print("paragraphs:")
    # for paragraph in paragraphs:
    #     print(paragraph)
    #     print("==="*10)

    for paragraph in paragraphs:
        if len(paragraph.strip()) == 0:
            continue  # 跳过空段落

        if chinese_character_count(current_chunk) + chinese_character_count(paragraph) <= max_chars:
            current_chunk += paragraph + "\n"
        else:
            if chinese_character_count(current_chunk) > 0:
                chunks.append(current_chunk.strip())
                current_chunk = ""

            if chinese_character_count(paragraph) <= max_chars:
                current_chunk = paragraph + "\n"
            else:
                # 对超长段落进行分割
                # print("segmenting paragraph...")
                # print("raw paragraph: ", paragraph)
                # print("==="*10)
                paras = seg_doc(paragraph)
                paras = paras.split("\n")
                paras = [para.strip() for para in paras if len(para.strip()) > 0]
                # print("segmented paras: ")
                # for para in paras:
                #     print(para)
                #     print("==="*10)

                temp_chunk = ""
                for para in paras:
                    if chinese_character_count(temp_chunk) + chinese_character_count(para) <= max_chars:
                        temp_chunk += para
                    else:
                        if chinese_character_count(temp_chunk) > 0:
                            chunks.append(temp_chunk.strip())
                        temp_chunk = para

                if chinese_character_count(temp_chunk) > 0:
                    current_chunk = temp_chunk + "\n"

    if chinese_character_count(current_chunk) > 0:
        chunks.append(current_chunk.strip())

    return chunks


